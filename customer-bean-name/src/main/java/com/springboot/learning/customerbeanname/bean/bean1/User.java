package com.springboot.learning.customerbeanname.bean.bean1;

import org.springframework.stereotype.Component;

@Component
public class User {
    String name;
    int age;

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
